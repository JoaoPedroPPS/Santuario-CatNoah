    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       
    Gato gato = new Gato();
    gato.setNome(txtNome.getText());
    gato.setPeso(Double.parseDouble(txtPeso.getText()));
    gato.setCor(txtCor.getText());
    gato.setIdade(Integer.parseInt(txtIdade.getText()));
    
    if(txtPosicao.getText().equals(""))
           listaGatos.add(gato);
    else
           listaGatos.add(Integer.parseInt(txtPosicao.getText()),gato);
      
    mostra();       
    }//GEN-LAST:event_btnAddActionPerformed

