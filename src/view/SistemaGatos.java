    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        // TODO add your handling code here:
        int i=0;
        boolean encontrou = false;
        for(Gato g:listaGatos){
            if(g.getNome().contains(txtNome.getText())){
                JOptionPane.showMessageDialog
                    (null, "Encontrado posicao "+i);
                encontrou = true;
            }
            i++;
        }// fim for
        if(!encontrou)
            JOptionPane.showMessageDialog
                    (null, "Não encontrado ");  
        
        
    }//GEN-LAST:event_btnBuscaActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
         Gato gato;
        if(!listaGatos.isEmpty()){
            if(txtRemove.getText().equals(""))
                gato = listaGatos.remove();
            else
                gato = listaGatos.remove(
                    Integer.parseInt(txtRemove.getText()) );
            
            lblProx.setText(gato.getNome());
            mostra();
        
    }//GEN-LAST:event_btnRemoveActionPerformed
    }
    private void btnNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNomeActionPerformed
        // TODO add your handling code here:
        Collections.sort(listaGatos);
        mostra();
        
    }//GEN-LAST:event_btnNomeActionPerformed

